num1 = int(input('Enter a integer number: '))
num2 = int(input('Enter another integer number: '))
num3 = float(input('Enter a real number: '))

solution1 = (num1 * 2) * (num2 / 2)
solution2 = (num1 * 3) + num3
solution3 = num3 ** 3

print('The product of twice the first and half the second: ', solution1)
print('The sum of three times the first and third: ', solution2)
print('The third raised to the cube: ', solution3)
